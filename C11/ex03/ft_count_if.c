int	ft_count_if(char **tab, int length, int (*f)(char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!tab)
		return (0);
	while (length--)
	{
		if (*tab)
		{
			if (f(*tab))
				i++;
		}
		tab++;
	}
	return (i);
}
